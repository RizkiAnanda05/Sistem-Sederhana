    public void tampiltable(){
        DefaultTableModel tb = new DefaultTableModel();
        tabelmurid.setModel(tb);
        
        tb.addColumn("ID");
        tb.addColumn("Nama");
        tb.addColumn("Jenis Kelamin");
        tb.addColumn("Jurusan");
        
        
        try {
            String sql = "SELECT * FROM murid WHERE nama_murid LIKE '%"+txtcari1.getText()+"%' OR id = '"+txtcari1.getText()+"'";
            java.sql.Connection conn=(Connection)konfig.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                tb.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            }
            tabelmurid.setModel(tb);
        } catch (Exception e) {
        
        }
    }
