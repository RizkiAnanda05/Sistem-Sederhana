    private void tabelmuridMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelmuridMouseClicked
        // TODO add your handling code here:
        int i = tabelmurid.rowAtPoint(evt.getPoint());
        txt_idMurd.setText(tabelmurid.getValueAt(i, 0).toString());
        txt_namaMurd.setText(tabelmurid.getValueAt(i, 1).toString());
        String JK = tabelmurid.getValueAt(i, 2).toString();
        if(JK.equals("L")){
            Rb_lk.setSelected(true);
        }else{
            Rb_pr.setSelected(true);
        }
        String Ka = tabelmurid.getValueAt(i, 3).toString();
        Cb_jrs.setSelectedItem(Ka);
    }//GEN-LAST:event_tabelmuridMouseClicked

